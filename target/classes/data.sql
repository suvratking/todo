DROP TABLE IF EXISTS todo;

CREATE TABLE todo (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
   title VARCHAR(255),
   completed BOOLEAN NOT NULL,
   CONSTRAINT pk_todo PRIMARY KEY (id)
);


